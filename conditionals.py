#Conditionals

x = 4

if x < 6:
    print('This is true')
else:
    print('This is not true')


color = 'red'

if color == 'red':
    print('The color is red')

elif color == 'blue':
    print('The color is blue')

else:
    print('That color is neither red or blue')


#Logical Operators
if color == 'red' and x < 6:
    print('The color is red and x is less than 6')
else:
    print('Change one of the variables to fit the if statement')